// Operators in JS :-

let a = 10;

let b = 5;

console.log(`${a} + ${b} = ${a+b}`);
console.log(`${a} - ${b} = ${a-b}`);
console.log(`${a} * ${b} = ${a*b}`);
console.log(`${a} / ${b} = ${a/b}`);
console.log(`${a} % ${b} = ${a%b}`);
console.log(`${a} to the power ${b} = ${a**b}`);

function sum(){
    return a+b;
}
console.log(sum());

